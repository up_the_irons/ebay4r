    endpoint_url ||= DefaultEndpointUrl
    super(endpoint_url, nil)
    self.mapping_registry = ::EBay::DefaultMappingRegistry::EncodedRegistry
    self.literal_mapping_registry = ::EBay::DefaultMappingRegistry::LiteralRegistry
